<section>
<p><h2 id = "risoto"><i>Para o risoto</i></p></h2>

<b>INGREDIENTES</b>
<br />

<ul class="a">
    <li>300 g de camarão cinza médio limpos (cerca de 26 unidades)</li>
    <li>1 xícara (chá) de arroz para risoto </li>
    <li>1 alho-poró</li>
    <li>1 cebola</li>
    <li>¼ de xícara (chá) de vinho branco</li> 
    <li>1,250 litro do caldo de camarão</li>
    <li>azeite a gosto</li> 
    <li>sal e pimenta-do-reino moída na hora a gosto</li>
    <li>broto de agrião a gosto para servir</li> 
    <li>broto de rúcula a gosto para servir</li>
    <li>cerefólio a gosto para servir</li> 
    <li>farofinha de pão a gosto para servir</li> 
    <li>1 xícara (chá) de óleo para fritar o alho-poró</li> 
</ul>

<b>MODO DE PREPARO</b>
<br />

<ol class="c">
    <li>Numa panela, leve o caldo de camarão ao fogo alto. Quando ferver, abaixe o 
    fogo e mantenha o caldo aquecido durante todo o preparo.</li>
    <li>Corte um pedaço de 5 cm do alho-poró, mais próximo da parte verde. Corte o 
    pedaço ao meio no sentido do comprimento e fatie fino também no sentido do 
    comprimento, reserve para a decoração. Corte o restante do talo do alho-poró 
    ao meio, no sentido do comprimento, e fatie cada metade em meias-luas finas. 
    Descasque e pique fino a cebola.</li>
    <li>Leve uma panela média ao fogo médio, regue com 1 colher (sopa) de azeite, 
    adicione a cebola e o alho-poró. Tempere com uma pitada de sal e refogue 
    até murchar, por cerca de 3 minutos. Acrescente o arroz e mexa bem por 2
    minutos para envolver os grãos com azeite. Tempere com sal e pimenta-do-reino – 
    lembre-se de que o caldo caseiro não leva sal!</li>
    <li>Regue com o vinho e misture até secar. Adicione 2 conchas do caldo e misture 
    bem. Mexa de vez em quando, até secar. Repita o procedimento, adicionando o 
    caldo, de concha em concha, mexendo, até o risoto ficar no ponto, por cerca 
    de 15 minutos – o grão deve estar cozido mas ainda durinho no centro (al dente). 
    Enquanto isso, prepare o alho-poró da finalização.</li>
    <li>Leve uma panela pequena de borda alta com óleo ao fogo médio para aquecer. 
    Enquanto isso, forre uma travessa com papel-toalha. Assim que o óleo estiver 
    quente, com cuidado, mergulhe o alho-poró cortado para fritar por cerca de 
    30 segundos. Transfira o alho-poró para a travessa com papel toalha e reserve.</li>
    <li>Atenção: na última adição de caldo, misture o camarão (exceto os 12 que 
    ficaram com o rabinho) e deixe cozinhar por cerca de 2 minutos. O camarão 
    deve ficar cozido e o risoto bem úmido. Acrescente 2 colheres (sopa) de azeite 
    e misture bem. Tampe a panela e doure os camarões da finalização.</li>
    <li>Tempere os 12 camarões (que permaneceram com o rabinho) com sal e pimenta a 
    gosto. Leve uma frigideira, de preferência antiaderente, ao fogo médio para 
    aquecer. Regue com 1 colher (sopa) de azeite e disponha os 12 camarões. 
    Doure por cerca de 1 minuto de cada lado e, com uma pinça, transfira para um prato.</li>
    <li>Sirva o risoto com os camarões dourados, o alho-poró frito, cerefólio, 
    farofinha de pão e os brotinhos de agrião e rúcula.</li>
</ol>
