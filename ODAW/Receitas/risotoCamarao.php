<p><h2 id = "caldo"><i>Para o caldo</i></p></h2>
<b>INGREDIENTES</b>
<br />
<ul class="a">
    <li>500 g de camarão cinza médio inteiros (com casca e cabeça)</li>
    <li>½ cebola</li>
    <li>1 cenoura</li>
    <li>2 dentes de alho</li>
    <li>3 ramos de tomilho</li>
    <li>2 litros de água</li>
    <li>3 colheres (sopa) de vinho branco</li>
    <li>Folhas de 1 alho-poró</li>
</ul>

<b>MODO DE PREPARO</b>
<br />

<ol class="c">
    <li>Limpe os camarões: cascas e cabeças serão usadas para fazer o caldo e os 
    camarões limpos no risoto. Primeiro, separe 12 camarões e tire as cabeças e 
    as cascas, mas mantenha o rabo, assim ele fica mais bonito na apresentação. 
    Com os demais, retire as cabeças e cascas completamente.</li>
    <li>Com um palito de dentes faça um furo próximo da cauda e puxe a tripinha 
    delicadamente para cima (se romper, fure mais adiante e recomece). Se 
    preferir, com uma faca pequena bem afiada, faça um corte raso no dorso, 
    de ponta a ponta e descarte a tripinha. No total você terá cerca de 200g de 
    cabeças e cascas para o caldo e 300g de camarões limpos para o risoto.</li>
    <li>Em uma peneira, lave as cascas e cabeças sob a água corrente. Com a lateral 
    da lâmina da faca, amasse e descasque os dentes de alho. Descasque e corte a
    cebola em meias-luas finas. Lave e, sem descascar, corte a cenoura em rodelas de 1 cm.</li>
    <li>Numa panela grande, leve todos os ingredientes ao fogo alto. Quando ferver, 
    abaixe o fogo e conte 40 minutos para o caldo reduzir e apurar os sabores</li>
    <li>Passado os 40 minutos, sobre uma tigela, passe o caldo por uma peneira 
    pressionando levemente com uma colher para extrair todo o líquido. Reserve.</li>
</ol>
<p>OBS: você pode preparar o caldo de camarão com antecedência e manter na geladeira por até 2 dias.</p>
